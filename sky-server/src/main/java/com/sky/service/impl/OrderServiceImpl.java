    /**
     * 支付成功，修改订单状态
     *
     * @param outTradeNo
     */
    public void paySuccess(String outTradeNo) {

        // 根据订单号查询订单
        Orders ordersDB = orderMapper.getByNumber(outTradeNo);

        // 根据订单id更新订单的状态、支付方式、支付状态、结账时间
        Orders orders = Orders.builder()
                .id(ordersDB.getId())
                .status(Orders.TO_BE_CONFIRMED)
                .payStatus(Orders.PAID)
                .checkoutTime(LocalDateTime.now())
                .build();

        orderMapper.update(orders);

        //通过websocket向客户端浏览器推送信息
        Map map = new HashMap<>();
        map.put("type", 1); // 1 表示来单提醒 2 表示客户催单
        map.put("orderId", ordersDB.getId());
        map.put("content", "订单号: " + outTradeNo);

        String json = JSON.toJSONString(map);
        webSocketServer.sendToAllClient(json);
    }

    /**
     * 根据订单ID查询订单详细信息
     * @param id 订单ID
     * @return OrderVO
     */
    @Override
    public OrderVO details(Long id) {
        //通过订单ID查询订单信息
        Orders order = orderMapper.getById(id);
        //通过订单id查询明细信息
        List<OrderDetail> list = orderDetailMapper.getByOrderId(id);
        //创建OrderVO对象用于返回数据
        OrderVO orderVO = new OrderVO();
        //拷贝查询到的数据到OrderVO对象
        BeanUtils.copyProperties(order, orderVO);
        //把查询到的订单明细赋值到OrderVO的成员变量上
        orderVO.setOrderDetailList(list);
        //返回数据
        return orderVO;
    }

    /**
     * 查询历史订单
     * @param page 页面
     * @param pageSize 每页记录数
     * @param status 订单状态
     * @return 分页查询的结果
     */
    @Override
    public PageResult pageQuery(Integer page, Integer pageSize, Integer status) {
        //获取当前用户id
        Long userId = BaseContext.getCurrentId();

        //分页查询
        PageHelper.startPage(page, pageSize);
        Page<Orders> order = orderMapper.pageQuery(page, pageSize, status, userId);

        long total = order.getTotal();
        List<Orders> orderList = order.getResult();

        //创建OrderVO对象
        List<OrderVO> orderVOList = new ArrayList<>();

        //把查询到的订单复制到OrderVO集合
        for (Orders o : orderList) {
            OrderVO tempOrderVO = new OrderVO();
            BeanUtils.copyProperties(o, tempOrderVO);
            List<OrderDetail> currOrderDetails = orderDetailMapper.getByOrderId(o.getId());
            tempOrderVO.setOrderDetailList(currOrderDetails);
            orderVOList.add(tempOrderVO);
        }

        return new PageResult(total, orderVOList);
    }

